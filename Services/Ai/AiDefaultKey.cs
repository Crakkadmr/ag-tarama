using System.Text;

namespace AgTarama.Services.Ai;

public static class AiDefaultKey
{
    private const byte XorKey = 0x5A;

    private static readonly byte[] Encoded =
    [
        0x29, 0x31, 0x77, 0x35, 0x28, 0x77, 0x2C, 0x6B, 0x77, 0x63, 0x63, 0x6B, 0x69, 0x3E, 0x39, 0x6E,
        0x69, 0x6B, 0x6B, 0x3B, 0x6C, 0x62, 0x6E, 0x3F, 0x6D, 0x3B, 0x63, 0x6F, 0x6B, 0x6A, 0x3C, 0x39,
        0x38, 0x38, 0x39, 0x6A, 0x68, 0x3C, 0x3E, 0x6E, 0x6C, 0x3B, 0x3B, 0x3C, 0x6C, 0x6C, 0x6F, 0x3C,
        0x6D, 0x3C, 0x3B, 0x6E, 0x3C, 0x6D, 0x63, 0x62, 0x6E, 0x6D, 0x6C, 0x6D, 0x3B, 0x69, 0x3C, 0x39,
        0x3B, 0x63, 0x63, 0x6B, 0x6F, 0x6A, 0x6E, 0x3F, 0x3C
    ];

    public static string Get()
    {
        var bytes = Encoded.Select(b => (byte)(b ^ XorKey)).ToArray();
        return Encoding.UTF8.GetString(bytes);
    }
}
